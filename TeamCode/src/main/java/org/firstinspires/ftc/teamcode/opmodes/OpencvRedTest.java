/*
 * This OpMode scans a single servo back and forward until Stop is pressed.
 * The code is structured as a LinearOpMode
 * INCREMENT sets how much to increase/decrease the servo position each cycle
 * CYCLE_MS sets the update period.
 *
 * This code assumes a Servo configured with the name "left_hand" as is found on a Robot.
 *
 * NOTE: When any servo position is set, ALL attached servos are activated, so ensure that any other
 * connected servos are able to move freely before running this test.
 *
 * Use Android Studio to Copy this Class, and Paste it into your team's code folder with a new name.
 * Remove or comment out the @Disabled line to add this OpMode to the Driver Station OpMode list
 */
@TeleOp(name = "Test: Opencv Red Test", group = "ZTest")
//@Disabled
public class OpencvRedTest extends LinearOpMode {

    private RedFinder visionProcessor = new RedFinder();
    private VisionPortal visionPortal;
    private AprilTagProcessor aprilTag;              // Used for managing the AprilTag detection process.
    private AprilTagDetection desiredTag = null;     // Used to hold the data for a detected AprilTag
    //int DESIRED_TAG_ID = 5;    // Choose the tag you want to approach or set to -1 for ANY tag.

    private ElapsedTime runtime = new ElapsedTime();

    @Override
    public void runOpMode() {
        initAprilTag();

        initRedFinding();
        // Wait for the start button
        telemetry.addData(">", "Press Start to start opencv test.");
        telemetry.update();
        RedFinder.Selected selected;
        waitForStart();


        // Scan servo till stop pressed.
        while (opModeIsActive()) {

            // slew the servo, according to the rampUp (direction) variable.
            // RedFinder.print_selection(telemetry);
            visionProcessor.print_selection();
            telemetry.addData(">", "Press X for moving up");
            telemetry.addData(">", "Press Y for moving down");
            telemetry.addData(">", "Press A for stop");
           if (gamepad1.a) {
           }
            // Display the current value
            telemetry.addData(">", "Press Stop to end test.");
            telemetry.update();


            // Display the current value

        }


    }
    void initRedFinding() {
        //visionProcessor = new RedFinder();
        visionProcessor.setTelemetry(telemetry);
        visionPortal = VisionPortal.easyCreateWithDefaults(hardwareMap.get(WebcamName.class, "Webcam 1"), visionProcessor, aprilTag);

    }
    private void initAprilTag() {
        // Create the AprilTag processor by using a builder.
        aprilTag = new AprilTagProcessor.Builder().build();

        // Adjust Image Decimation to trade-off detection-range for detection-rate.
        // eg: Some typical detection data using a Logitech C920 WebCam
        // Decimation = 1 ..  Detect 2" Tag from 10 feet away at 10 Frames per second
        // Decimation = 2 ..  Detect 2" Tag from 6  feet away at 22 Frames per second
        // Decimation = 3 ..  Detect 2" Tag from 4  feet away at 30 Frames Per Second
        // Decimation = 3 ..  Detect 5" Tag from 10 feet away at 30 Frames Per Second
        // Note: Decimation can be changed on-the-fly to adapt during a match.
        aprilTag.setDecimation(2);
    }
}


